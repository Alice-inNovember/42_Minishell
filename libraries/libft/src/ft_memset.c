#include "../includes/libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ptr;
	unsigned char	character;
	size_t			i;

	ptr = (unsigned char *)b;
	character = (unsigned char)c;
	i = 0;
	while (i < len)
	{
		ptr[i] = character;
		i++;
	}
	return (ptr);
}
