#include "../includes/libft.h"

void	*ft_malloc(size_t size)
{
	void	*ptr;

	ptr = malloc(size);
	if (ptr == NULL)
	{
		perror("malloc error occurred");
		exit(EXIT_FAILURE);
	}
	return (ptr);
}
