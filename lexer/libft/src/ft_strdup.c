#include "../includes/libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	length;
	size_t	i;

	length = ft_strlen(s1);
	i = 0;
	str = (char *)ft_calloc((length + 1), sizeof(char));
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
